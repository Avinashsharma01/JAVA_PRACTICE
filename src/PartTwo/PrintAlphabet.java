package PartTwo;

public class PrintAlphabet {
    public static void main(String[] args) {

        for (char a='A'; a<='Z'; a++){
            System.out.print(a + " ");
        }

        System.out.println(" ");

        for (char b='a'; b<='z'; b++){
            System.out.print(b + " ");
        }
    }
}
